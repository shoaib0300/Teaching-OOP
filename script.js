

// How to find limited numbers even and Odd

let number = prompt("Please enter how many numbers you want to check for even and odd: ");

for (let i = 1; i <= number; i++) {
    if (i % 2 !== 0) {
        console.log(i + " is odd");
    } else {
        console.log(i + " is even");
    }
}



